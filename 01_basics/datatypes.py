
# Data Types in Python


# Numbers
age = 30            # int (integer)
temperature = 36.6  # float (decimal number)

print("Age:", age)
print("Temperature:", temperature)

# Strings
city_name = "Paris"
greeting = "Hello, World!"

print(city_name)
print(greeting)

# Boolean
is_sunny = True
is_raining = False

print("Sunny?", is_sunny)
print("Raining?", is_raining)

# List (ordered collection)
fruits = ["apple", "banana", "cherry"]
print("Fruits:", fruits)

# Tuple (ordered, immutable collection)
coordinates = (10.0, 20.0)
print("Coordinates:", coordinates)

# Set (unordered, unique collection)
unique_numbers = {1, 2, 2, 3, 3, 4}
print("Unique numbers:", unique_numbers)

# Dictionary (key-value pairs)
person_info = {"name": "Bob", "age": 28, "city": "London"}
print("Person info:", person_info)

# ðŸ§  Note: Python automatically detects the type
print("Type of person_info:", type(person_info))
