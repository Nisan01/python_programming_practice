
#Operators in Python


# Arithmetic
x, y = 10, 3
print("x+y =", x+y)
print("x-y =", x-y)
print("x*y =", x*y)
print("x/y =", x/y)
print("x//y =", x//y)
print("x%y =", x%y)
print("x**y =", x**y)

# Comparison
print("x==y?", x==y)
print("x!=y?", x!=y)
print("x>y?", x>y)

# Logical
a, b = True, False
print("a and b:", a and b)
print("a or b:", a or b)
print("not a:", not a)

# Assignment
n = 5
n += 2
print("n:", n)

# Membership
lst = ["red","blue"]
print("'red' in lst?", "red" in lst)

# Identity
p = [1,2]; q = p; r = [1,2]
print("p is q?", p is q)
print("p is r?", p is r)
